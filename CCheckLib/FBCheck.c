// 光功率计测试
uint8_t check(uint8_t *buff, int16_t len)
{
    int16_t i;
    uint32_t CHECK = 0;
    // 校验 CHECK=(~（帧头+地址+长度+命令+数据）)+1；
    for(i=0; i<len; i++)
    {
        CHECK+=buff[i];
    }
    CHECK = (~CHECK) +1;

    return CHECK;
}


